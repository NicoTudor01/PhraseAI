-- DATA ARCHITECT: Restore missing auth-user foreign keys on pre-existing versions of the new tables.
do $$
declare
  table_name text;
  constraint_name text;
begin
  foreach table_name in array array['email_history', 'persona_snapshots', 'style_tags']
  loop
    constraint_name := table_name || '_user_id_fkey';
    if not exists (
      select 1
      from pg_constraint c
      where c.conrelid = format('public.%I', table_name)::regclass
        and c.contype = 'f'
        and c.confrelid = 'auth.users'::regclass
    ) then
      execute format(
        'alter table public.%I add constraint %I foreign key (user_id) references auth.users(id) on delete cascade',
        table_name,
        constraint_name
      );
    end if;
  end loop;
end;
$$;

-- DATA ARCHITECT: Maintain modification and finalization timestamps without relying on application callers.
create or replace function public.set_data_architect_updated_at()
returns trigger
language plpgsql
security invoker
set search_path = public
as $$
begin
  new.updated_at = now();
  if tg_table_name = 'email_history'
     and new.final_version is not null
     and new.finalized_at is null then
    new.finalized_at = now();
  end if;
  return new;
end;
$$;

