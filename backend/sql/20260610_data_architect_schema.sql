-- DATA ARCHITECT: Preserve the existing backend contract while exposing explicit current-style and freshness columns.
create table if not exists public.style_profiles (
  user_id uuid primary key references auth.users(id) on delete cascade,
  profile jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now(),
  current_style jsonb not null default '{}'::jsonb,
  last_updated timestamptz not null default now()
);

alter table public.style_profiles
  add column if not exists profile jsonb,
  add column if not exists updated_at timestamptz,
  add column if not exists current_style jsonb,
  add column if not exists last_updated timestamptz;

update public.style_profiles
set
  profile = coalesce(profile, current_style, '{}'::jsonb),
  current_style = coalesce(current_style, profile, '{}'::jsonb),
  updated_at = coalesce(updated_at, last_updated, now()),
  last_updated = coalesce(last_updated, updated_at, now())
where
  profile is null
  or current_style is null
  or updated_at is null
  or last_updated is null;

alter table public.style_profiles
  alter column profile set default '{}'::jsonb,
  alter column profile set not null,
