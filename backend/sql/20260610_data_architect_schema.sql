-- DATA ARCHITECT: Synchronize old and new style-profile column names for backward-compatible reads and writes.
create or replace function public.sync_style_profile_columns()
returns trigger
language plpgsql
security invoker
set search_path = public
as $$
begin
  if tg_op = 'INSERT' then
    if new.current_style is distinct from '{}'::jsonb
       and new.profile is not distinct from '{}'::jsonb then
      new.profile = new.current_style;
    else
      new.current_style = new.profile;
    end if;
  elsif new.current_style is distinct from old.current_style
        and new.profile is not distinct from old.profile then
    new.profile = new.current_style;
  else
    new.current_style = new.profile;
  end if;

  new.updated_at = now();
  new.last_updated = new.updated_at;
  return new;
end;
$$;

drop trigger if exists style_profiles_set_updated_at on public.style_profiles;
drop trigger if exists style_profiles_sync_columns on public.style_profiles;
create trigger style_profiles_sync_columns
before insert or update on public.style_profiles
for each row execute function public.sync_style_profile_columns();

-- DATA ARCHITECT: Preserve legacy text ownership untouched and add the auth-user foreign key to UUID deployments.
do $$
declare
  user_id_type text;
begin
  select data_type
  into user_id_type
  from information_schema.columns
  where table_schema = 'public'
    and table_name = 'style_profiles'
    and column_name = 'user_id';

  if user_id_type <> 'uuid' then
    raise notice 'style_profiles.user_id remains legacy text; run auth_hardening_migration.sql after reviewing unmatched rows';
  end if;

  if user_id_type = 'uuid'
     and not exists (
       select 1
       from pg_constraint c
       join pg_class t on t.oid = c.conrelid
       join pg_namespace n on n.oid = t.relnamespace
       where n.nspname = 'public'
         and t.relname = 'style_profiles'
         and c.contype = 'f'
         and c.confrelid = 'auth.users'::regclass
     ) then
    alter table public.style_profiles
      add constraint style_profiles_user_id_fkey
      foreign key (user_id) references auth.users(id) on delete cascade;
  end if;
end;
$$;

