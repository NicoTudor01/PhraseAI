-- DATA ARCHITECT: Keep feedback timestamps fresh without mutating immutable event content.
create or replace function public.set_style_feedback_updated_at()
returns trigger
language plpgsql
security invoker
set search_path = public
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists style_feedback_set_updated_at on public.style_feedback;
create trigger style_feedback_set_updated_at
before insert or update on public.style_feedback
for each row execute function public.set_style_feedback_updated_at();

