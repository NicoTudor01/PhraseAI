do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.style_profiles'::regclass
      and conname = 'style_profiles_profile_completeness_check'
  ) then
    alter table public.style_profiles
      add constraint style_profiles_profile_completeness_check
      check (profile_completeness >= 0 and profile_completeness <= 1) not valid;
  end if;

  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.style_profiles'::regclass
      and conname = 'style_profiles_emails_analyzed_check'
  ) then
    alter table public.style_profiles
      add constraint style_profiles_emails_analyzed_check
      check (emails_analyzed >= 0) not valid;
  end if;
end;
$$;

-- DATA ARCHITECT: Keep JSON and first-class columns synchronized for old and new clients.
create or replace function public.sync_style_profile_columns()
returns trigger
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_profile jsonb;
begin
  if tg_op = 'INSERT' then
    if new.current_style is distinct from '{}'::jsonb
       and new.profile is not distinct from '{}'::jsonb then
      new.profile = new.current_style;
    else
      new.current_style = new.profile;
    end if;
  elsif new.current_style is distinct from old.current_style
        and new.profile is not distinct from old.profile then
    new.profile = new.current_style;
  else
    new.current_style = new.profile;
  end if;

  v_profile := coalesce(new.profile, '{}'::jsonb);
  new.persona_label = coalesce(nullif(new.persona_label, ''), nullif(v_profile ->> 'persona_label', ''), 'Emerging Voice');
  new.persona_summary = coalesce(nullif(new.persona_summary, ''), nullif(v_profile ->> 'persona_summary', ''), 'PhraseAI is still learning this writing style.');
  new.profile_completeness = greatest(0, least(1, coalesce(new.profile_completeness, (v_profile ->> 'profile_completeness')::numeric, 0)));
  new.emails_analyzed = greatest(0, coalesce(new.emails_analyzed, (v_profile ->> 'emails_analyzed')::integer, ((v_profile -> 'stats') ->> 'learned_examples')::integer, 0));
  new.updated_at = now();
  new.last_updated = new.updated_at;
  return new;
end;
$$;

-- DATA ARCHITECT: One immutable public feedback event per user action, including manual edited rewrites.
create table if not exists public.style_feedback (
  id bigint generated by default as identity primary key,
  rewrite_id bigint not null references public.email_history(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  feedback_type text not null check (feedback_type in ('good', 'off', 'edited')),
  trait_affected text,
  user_note text,
  manual_edit text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- DATA ARCHITECT: Make the feedback migration idempotent for existing partially-created tables.
alter table public.style_feedback
