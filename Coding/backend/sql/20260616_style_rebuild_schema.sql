-- DATA ARCHITECT: Enable and replace feedback RLS with strict owner plus rewrite ownership checks.
alter table public.style_feedback enable row level security;

do $$
declare
  policy_record record;
begin
  for policy_record in
    select schemaname, tablename, policyname
    from pg_policies
    where schemaname = 'public'
      and tablename = 'style_feedback'
  loop
    execute format(
      'drop policy if exists %I on %I.%I',
      policy_record.policyname,
      policy_record.schemaname,
      policy_record.tablename
    );
  end loop;
end;
$$;

-- DATA ARCHITECT: Users can read only feedback events attached to their own rewrite history.
create policy style_feedback_select_own
on public.style_feedback for select to authenticated
using (
  auth.uid() = user_id
  and exists (
    select 1 from public.email_history h
    where h.id = rewrite_id and h.user_id = auth.uid()
  )
);

-- DATA ARCHITECT: Users can write only feedback events attached to their own rewrite history.
create policy style_feedback_insert_own
on public.style_feedback for insert to authenticated
with check (
  auth.uid() = user_id
  and exists (
    select 1 from public.email_history h
    where h.id = rewrite_id and h.user_id = auth.uid()
  )
);

-- DATA ARCHITECT: Users can amend their own feedback notes/manual edits only.
